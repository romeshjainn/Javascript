// Declaring An Array
const laptopSpecs = ["Lenovo", 512, "i3 11th Gen"];

// Accessing Array Elements
console.log(laptopSpecs[0]); // Lenovo

// Modifying Array Element
laptopSpecs[0] = "Samsung";
console.log(laptopSpecs[0]); // Samsung

// Check Length Of An Array
console.log(laptopSpecs.length); // 3

// Iterating Over An Array
for (let i = 0; i < laptopSpecs.length; i++) {
    console.log(laptopSpecs[i]);
}



