// Using If Else
let num = 5;
if (num % 2 == 0) {
    console.log(num);
}
else {
    console.log("Its not an even number");
}

// Using Ternary Operator
const result = num % 2 ? "Its not an even number" : "Its an even number";
console.log(result);
let age = 17;
const eligible = age <= 18 ? "You cannot vote" : "You can vote";
console.log(eligible);
